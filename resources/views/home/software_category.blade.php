<script>
    document.addEventListener('DOMContentLoaded', () => {
    const accordionLabels = document.querySelectorAll('.accordion_tab-label');
    const showMoreButton = document.getElementById('showMoreButton');
    const showLessButton = document.getElementById('showLessButton');
    const moreItems = document.querySelectorAll('.more-items');

    accordionLabels.forEach(label => {
        label.addEventListener('click', () => {
            const content = label.nextElementSibling;
            const isActive = label.classList.contains('active');
            
            // Hide all other content and remove border from other labels
            accordionLabels.forEach(lbl => {
                lbl.classList.remove('active');
                lbl.nextElementSibling.style.display = 'none';
            });

            // Show the clicked content and add border to the clicked label
            if (!isActive) {
                content.style.display = 'block';
                label.classList.add('active');
            } else {
                content.style.display = 'none';
                label.classList.remove('active');
            }
        });
    });

    showMoreButton.addEventListener('click', () => {
        moreItems.forEach(item => item.classList.remove('d-none'));
        showMoreButton.classList.add('d-none');
        showLessButton.classList.remove('d-none');
    });

    showLessButton.addEventListener('click', () => {
        moreItems.forEach(item => item.classList.add('d-none'));
        showMoreButton.classList.remove('d-none');
        showLessButton.classList.add('d-none');
    });
});
</script>